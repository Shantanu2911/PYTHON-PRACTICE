import turtle
t = turtle.Turtle()
t.speed(2)
t.pensize(20)
t.penup()
t.setposition(0,0)
t.pendown()
t.begin_fill()
t.fillcolor("red")
t.pencolor("black")
t.forward(200)
t.left(90)
t.circle(250,180)
t.right(90)
t.back(200)
t.end_fill()
t.penup()
t.begin_fill()
t.fillcolor("white")
t.pencolor("black")
t.setposition(-51,51)
t.pendown()
t.circle(60)
t.end_fill()
t.penup()
t.setposition(-300,0)
t.pendown()
t.pencolor("black")
t.left(90)
t.circle(250,180)
t.hideturtle()
turtle.done()